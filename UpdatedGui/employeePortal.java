    private void borrowerCreateAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrowerCreateAccountButtonActionPerformed

        
        String usertype=(String)jComboBox2.getSelectedItem();
        
        String address= addressBorrowerCreateAccount.getText();
        
        String city= cityBorrowerCreateAccount.getText();
        
        String email= emailBorrowerCreateAccount.getText();
        
        String phone= phoneBorrowerCreateAccount.getText();
        
        String password= jPasswordField1.getText();
        
        boolean success=true;
        StringBuilder errorMessage=new StringBuilder("Kindly enter ");
       
       // displaying error(s) or information
       
       
      
      if(usertype.equals("Employee"))
               errorMessage=errorMessage.append("Employee's ");
       else
               errorMessage=errorMessage.append("Organization's ");
       
       
      if(phone.isEmpty()){
        // if(!success)
          //     errorMessage=errorMessage.append(", ");
         errorMessage=errorMessage.append("Phone Number");
         success=false;
       }
           
       if(address.isEmpty()){
         if(!success)
               errorMessage=errorMessage.append(", ");
         errorMessage=errorMessage.append("Address");
         success=false;
       }
       if(city.isEmpty()){
           if(!success)
               errorMessage=errorMessage.append(", ");
         errorMessage=errorMessage.append("City");
         success=false;
       }
       
       if(email.isEmpty()){
           if(!success)
               errorMessage=errorMessage.append(", ");
         errorMessage=errorMessage.append("Email");       
         success=false;
       }
       
       if(password.isEmpty()){
           if(!success)
               errorMessage=errorMessage.append(", ");
         errorMessage=errorMessage.append("Passoword");       
         success=false;
       }
        
       if(!password.isEmpty())
      {
      boolean passwordValid=password.length()>=6;
      
      if(!passwordValid)
      {
          if(success)
          {
              //errorMessage=errorMessage.delete(0, errorMessage.length()-1);
              errorMessage=new StringBuilder("Password should be atleast six characters");        
          }
          
          else 
          {
               
               errorMessage=errorMessage.append("\nPassword should be atleast six characters");
          }
          
          success=false;
      }
      }
      
       
      if(!phone.isEmpty())
      {
      boolean phoneValid=PhoneValidation.validate(phone);
      
      if(!phoneValid)
      {
          if(success)
          {
              //errorMessage=errorMessage.delete(0, errorMessage.length()-1);
              errorMessage=new StringBuilder("Enter a Valid Phone Number");        
          }
          
          else 
          {
               
               errorMessage=errorMessage.append("\nEnter a Valid Phone Number");
          }
          
          success=false;
      }
      }
      
      if(!email.isEmpty())
      {
      boolean emailValid=EmailValidation .validate(email);
      
      if(!emailValid)
      {
          if(success)
          {
              //errorMessage=errorMessage.delete(0, errorMessage.length()-1);
              errorMessage=new StringBuilder("Enter a Valid Email");        
          }
          
          else 
          {
               
               errorMessage=errorMessage.append("\nEnter a Valid Email");
          }
          
          success=false;
      }
      }
      
      
      
     
      
      int index=errorMessage.lastIndexOf(",");
       if(index!=-1)
           errorMessage.insert(index, " and");
       
      errorMessage.append(".");
        
     if(success )
       {
       
           if(usertype.equals("Employee"))
           {
           Employee personObj=new Employee();
           
           
           personObj.setEmail(email);
           personObj.setPhonenumber(phone);
           personObj.setUsertype("emp");
           personObj.setPassword(password);
           personObj.setAddress(address);
           personObj.setCity(city);
           //personObj.setId(2);
           //personObj.setCnic("");
           boolean empAdded=Employee.getInstance().addEmployee(personObj);
           if(empAdded){
                JOptionPane.showMessageDialog(this,"Employee Resgistered Successfully");
                 SendMail.send(email, "Safcity Registration", 
                        "You have been Registered on Safecity", "l164120@lhr.nu.edu.pk", "pepsi123456789");
           }
           else 
                JOptionPane.showMessageDialog(this,"Employee already Resgistered");
           }
           else
           {
               Organization orgObj;
               
               if(usertype.equals("Police")){
                    orgObj=new PoliceStation();
                    orgObj.setOrganizationtype("police");
               }
               else{
                   orgObj=new RescueStation();
                   orgObj.setOrganizationtype("rescue");
               }
               orgObj.setEmail(email);
               orgObj.setLocation(address);
               orgObj.setPhonenumber(phone);
               orgObj.setPassword(password);
               orgObj.setCity(city);
               
               boolean orgAdded=Employee.getInstance().addOrganization(orgObj);
               if(orgAdded){
                   
                   SendMail.send(email, "Safcity Registration", 
                        "You have been Registered on Safecity", "l164120@lhr.nu.edu.pk", "pepsi123456789");
                JOptionPane.showMessageDialog(this,"Organization Resgistered Successfully");
                
               }
               else 
                JOptionPane.showMessageDialog(this,"Organization already Resgistered");
           }
       }
       
       else
           JOptionPane.showConfirmDialog(null,errorMessage,"Insufficient Information",JOptionPane.OK_CANCEL_OPTION,JOptionPane.ERROR_MESSAGE);
        
     
        
    }//GEN-LAST:event_borrowerCreateAccountButtonActionPerformed

